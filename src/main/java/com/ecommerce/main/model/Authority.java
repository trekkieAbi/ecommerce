package com.ecommerce.main.model;



public class Authority {
	private Integer id;
	
	
	private String name;
	
	



	public Authority(Integer id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	

	public Authority() {
		super();
	}



	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}



	



	

}
